//! R6 — THE FULL FUNCTIONAL E2E: a REAL subprocess agent CLI doing REAL work, GOVERNED + GATED +
//! EVIDENCED, on an ON-DISK shared estate store, distributed by a REAL council verdict.
//!
//! Everything here is real except that the wrapped CLI + council voters are deterministic shell
//! scripts (fake-but-real: genuine subprocesses doing genuine fs work + honoring the genuine
//! governance hook) so the test is hermetic + offline. The on-disk `brain.db` is shared by
//! governance + orchestration + council + agent, and the per-tool-call pre-hook is a real child
//! process that opens that SAME on-disk store and gates the wrapped CLI across the process boundary.
//!
//! We prove, end-to-end:
//!   (clean unit)  the wrapped CLI's real subprocess WRITES a real file (`out.txt`); the unit-level
//!                 governance gate ALLOWS; the per-tool-call hook ALLOWS; phase → `Approved`;
//!                 the real artifact + evidence land on the shared store. FILE EXISTS on disk.
//!   (secret unit) the unit's work embeds `AKIA…`; the per-tool-call hook DENIES the write (exit 2);
//!                 the wrapped CLI aborts → the forbidden file is NEVER written; phase → `Rejected`.
//!                 FILE IS ABSENT on disk.
//!   (persistence) ALL entities — session, both units, the claim(s), both phases, the approved
//!                 unit's output, AND the council task + verdict — persist on the ONE on-disk store,
//!                 read back via a FRESH `SqliteStore::open(path)` connection (cross-handle sharing).

